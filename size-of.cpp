#include <iostream>
using namespace std;

int main()
{
    long double apple;

    cout << "Size of an integer is: " << sizeof(int);
    cout << " bytes.\n";
    cout << "Size of long integer is: " << sizeof(long);
    cout << "\n";
    cout << "An apple can be eaten in " << sizeof(apple);
    cout << " bytes!\n";
    return 0;
}
